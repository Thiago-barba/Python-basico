# Descubra o time do coração
print ('Escolha o seu time do coração ditando do número 1 ao 4')
time=int(input('Digite o número do time '))
if time==4:
    print('Seu time do coração é a Ferroviária')
elif time==3:
    print('Seu time do coração é a São Paulo')
elif time==2:
    print('Seu time do coração é a Santos')
elif time==1:
    print('Seu time é o melhor do Mundo, Vai Corinthians')
else:
    print('Você não tem time')